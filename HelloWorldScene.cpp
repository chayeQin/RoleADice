#include "HelloWorldScene.h"

USING_NS_CC;

Scene* HelloWorld::createScene()
{
    // 'scene' is an autorelease object
    auto scene = Scene::create();
    
    // 'layer' is an autorelease object
    auto layer = HelloWorld::create();

    // add layer as a child to scene
    scene->addChild(layer);

    // return the scene
    return scene;
}

// on "init" you need to initialize your instance
bool HelloWorld::init()
{
    //////////////////////////////
    // 1. super init first
    if ( !Layer::init() )
    {
        return false;
    }
    
    ZipUtils::setPvrEncryptionKey(0x1997cf69,0x82190c8e,0x1153e1c6,0x86cc245c);
	CCSpriteFrameCache::sharedSpriteFrameCache()->addSpriteFramesWithFile("a.plist");
	CCSprite* sprite = CCSprite::createWithSpriteFrameName("path.png");
	sprite->setPosition(ccp(200, 200));
	this->addChild(sprite);
    
    return true;
}


